m1 = m2 = 1

proj = [[1 0; 0 0], [0 0; 0 1]]

M = [1 0; 0 1]

Ω2(Ωeff12, Ωeff22) = [Ωeff12 0; 0 Ωeff22] 

K(κ) = [0 κ; κ 0]

V(Ωeff12, Ωeff22, κ) = M*Ω2(Ωeff12, Ωeff22) - K(κ)

Q(Ωeff12, Ωeff22, κ, σ, i) = tr(proj[i]*V(Ωeff12, Ωeff22, κ)*σ*inv(M))